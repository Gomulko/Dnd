// ─── COMBINED & HELPERS ────────────────────────────────────────────────────

export const allSpells: Spell[] = [...cantrips, ...spellsLevel1];

export function getSpellById(id: string): Spell | undefined {
  return allSpells.find((s) => s.id === id);
}

export function getSpellsByClass(classId: string): Spell[] {
  return allSpells.filter((s) => s.classes.includes(classId));
}

export function getCantripsForClass(classId: string): Spell[] {
  return cantrips.filter((s) => s.classes.includes(classId));
}

export function getLevel1SpellsForClass(classId: string): Spell[] {
  return spellsLevel1.filter((s) => s.classes.includes(classId));
}

export function getSpellsBySchool(school: SpellSchool): Spell[] {
  return allSpells.filter((s) => s.school === school);
}

export function getRitualSpells(): Spell[] {
  return allSpells.filter((s) => s.ritual);
}
